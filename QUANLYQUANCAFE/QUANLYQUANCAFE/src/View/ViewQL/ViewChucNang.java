        String ma = txtMa.getText();
        boolean trangThai = rbtDay.isSelected();
        String tt = "";
        if (trangThai) {
            tt = "Đầy";
        } else {
            tt = "Trống";
        }
        KhuVucModel kv = new KhuVucModel(ma, ten, tt);
        JOptionPane.showMessageDialog(this, impl1.update(kv, ma));
