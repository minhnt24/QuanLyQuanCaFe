    private void fillData(KhuVuc kv) {
        txtMa.setText(kv.getMaKV());
        txtTen.setText(kv.getTenKV());
        String tt = kv.getTrangThai();
        if (tt.equalsIgnoreCase("Trống")) {
            rbtTrong.setSelected(true);
        } else {
            rbtDay.setSelected(true);
        }
    }

