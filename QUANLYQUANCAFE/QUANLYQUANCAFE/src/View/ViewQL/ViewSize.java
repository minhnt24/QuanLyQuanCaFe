    public void showData(List<Size> listS) {
        dtm.setRowCount(0);
        for (Size x : listS) {
            dtm.addRow(x.toRowDataSize());
        }
    }

    public void fillData(int index) {
        Size s = listS.get(index);
        txtID.setText(s.getId());
        txtSize.setText(s.getSize());
    }

